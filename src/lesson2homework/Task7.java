package lesson2homework;

//Необходимо вывести на экран числа от 5 до 1.
//При решении используйте операцию декремента(--)
public class Task7 {
    public static void main(String[] args) {
        int chis;
        for (int i = 5; i >= 1; i--) {
            chis = i;
            System.out.println(chis);
        }
    }
}
